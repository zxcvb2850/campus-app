export function transformType(type){
  switch (type){
    case 0 :
      return "食堂餐";
      break;
    case 1 :
      return "取快递";
      break;
    case 2 :
      return "帮购物";
      break;
    case 3 :
      return "周边小吃";
      break;
    case 4 :
      return "帮摄影";
      break;
    case 5 :
      return "其他";
      break;
  }
}
